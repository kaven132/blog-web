const Database = require('better-sqlite3');
const path = require('path');
const db = new Database(path.join(__dirname, '..', 'data.db'));

function generateId(prefix = 'art') {
  return prefix + '_' + Date.now().toString(36) + '_' + Math.random().toString(36).slice(2, 8);
}

function insertArticle(title, content, category, subcategory, tags, summary, isPinned = 0) {
  const id = generateId('art');
  const now = new Date().toISOString();
  const tagsStr = JSON.stringify(tags);

  db.prepare(`
    INSERT INTO articles (id, title, content, category, subcategory, tags, summary, cover_image, is_pinned, created_at, updated_at)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
  `).run(id, title, content, category, subcategory, tagsStr, summary, '', isPinned, now, now);

  return id;
}

function insertComment(articleId, author, content) {
  const id = 'cmt_' + Date.now().toString(36) + '_' + Math.random().toString(36).slice(2, 8);
  const now = new Date().toISOString();
  db.prepare('INSERT INTO comments (id, article_id, author, content, created_at) VALUES (?, ?, ?, ?, ?)')
    .run(id, articleId, author, content, now);
  return id;
}

